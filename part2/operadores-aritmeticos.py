# Suma
operandoA = 3
operandoB = 6
suma = operandoA + operandoB
# print('La suma es:',suma)
print(f'Resultado de la suma: {suma}')

# Resta
resta = operandoA - operandoB
print(f'Resultado de la resta: {resta}')

# Multiplicacion
multiplicar = operandoA * operandoB
print(f'La multiplicacion es: {multiplicar}')

# Division
dividir = operandoA / operandoB
print(f'Resultado de la division es: {dividir}')

# Division enteros sin decimales
div = operandoA // operandoB
print(f'La division sin decimales es: {div}')

# Residuo o modulo
modulo = operandoA % operandoB
print(f'El Residuo o modulo es: {modulo}')

# Operador exponente
exponente = operandoA ** operandoB
print(f'El exponente es: {exponente}')  # 3 ala 6
