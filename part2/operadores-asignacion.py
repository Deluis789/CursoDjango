# Reasignacion

# Asignacion
miVariable = 10
# miVariable = miVariable + 2 es lo mismo
# Reasignacion con Suma
miVariable += 2
print(miVariable)
# Reasignacion Restando
miVariable -= 2
print(miVariable)
# Reasignacion multiplicando
miVariable *= 3
print(miVariable)
# Reasigancion con division
miVariable /= 2
print(miVariable)
